def multiply(x, y):
    '''
    multiply x * y
    :param x:
    :param y:
    :return: result of multiplication
    '''
    return x * y


def power2(x):
    '''
    build square
    :param x:
    :return:
    '''
    return x * x

if __name__ == '__main__':
    print(multiply(3, 4))
    print(power2(2))